#desenvolva um programa que leia as duas notas de um aluno ,calcule e mostra a sua media
n1 = int(input('\033[0;31;47mqual é a sua primeira nota \033[m'))
n2 = int(input('\033[0;31;47mqual é a sua segunda nota \033[m'))
media = int((n1+n2)/2)
print('\033[0;31;40ma sua media foi {}\033[m'.format(media))
